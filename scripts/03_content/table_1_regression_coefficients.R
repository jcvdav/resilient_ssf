# Load packages ----------------------------------------------------------------
pacman::p_load(
  here,
  kableExtra,
  tidyverse
)

# Load data --------------------------------------------------------------------
coefs <- readRDS(file = here("data", "processed", "coefficient_estimates.rds"))
## PROCESSING ##################################################################

# X ----------------------------------------------------------------------------
coefs %>%
  mutate(number = paste0(round(estimate, 3), " (", round(std.error, 3), ")")) %>%
  select(dep, term, number) %>%
  pivot_wider(names_from = dep,
              values_from = number) %>%
  select(term, MHW, C19) %>%
  kable() %>%
  kable_styling()

